var el = 0;
export function get(num: number){
    el ++;
    return DATA.slice(DATA.length - num * el - 1, DATA.length - num * (el - 1))
}


export function getSliceOfArrayByTimestamp(from: number, to: number) {
    // Sort the array by time
    const array = DATA;
    array.sort((a, b) => a.time - b.time);

    // Create an empty array to store the results
    const results = [];

    // Iterate over the array
    for (const item of array) {
        // If the timestamp is in the range, add the item to the results array
        if (item.time >= from && item.time <= to) {
            results.push(item);
        }
    }
    results.sort((a, b) => a.time - b.time);

    // Return the results array
    return results;
}